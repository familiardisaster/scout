with open("/home/ubuntu/wordlists/" + path + "/" + f1,'r') as infile, open("/home/ubuntu/wordlists/" + path + "/" + f2,'a+') as outfile:
        for line in infile:
            urls = re.findall(regex, line)
            for url in urls:
                outfile.write(url)